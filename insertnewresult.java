    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String regno = jTextField1.getText();
        String physics = jTextField2.getText();
        String chemistry = jTextField3.getText();
        String maths = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/student_result_management_portal","root","micro_code01");
            
            Statement st=con.createStatement();
            //next we check if the data in st is contained in new variable rs
            ResultSet rs=st.executeQuery("Select * from student where regno = '" + regno + "' ");
            if(rs.next()){
                st.executeUpdate("insert into result(regno, physics, chemistry, maths, em, dbms) values('"+regno+"','"+physics+"','"+chemistry+"','"+maths+"','"+em+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Successfully added the student's subject scores.");
                setVisible(false);// y this again as once we filled data of 1ppl then for 2ppl it whould b blank not existing data
            new insertnewresult().setVisible(true);// once 2ppl data is filled it should b new n visible
                
            }   
            else{
                JOptionPane.showMessageDialog(null, "This roll no doesn't exist.");
            }
          
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

